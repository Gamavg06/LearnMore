export function getLanguage() {
  const saved = localStorage.getItem(LANG_KEY);
  return dictionary[saved] ? saved : "es";
}

export function applyLanguage(lang = getLanguage()) {
  if (!dictionary[lang]) lang = "es";
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[lang][key]) node.textContent = dictionary[lang][key];
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    if (dictionary[lang][key]) node.placeholder = dictionary[lang][key];
  });
  document.querySelectorAll("#languageToggle").forEach((button) => {
    button.textContent = lang.toUpperCase();
  });
}

export function initLanguage() {
  applyLanguage();
  document.querySelectorAll("#languageToggle").forEach((button) => {
    button.addEventListener("click", () => {
      const next = getLanguage() === "es" ? "en" : "es";
      localStorage.setItem(LANG_KEY, next);
      applyLanguage(next);
      // Disparar evento para que otros módulos sepan que cambió el idioma
      window.dispatchEvent(new Event("learnmore:language-change"));
    });
  });
}

export function translate(key, lang = getLanguage()) {
  if (!dictionary[lang]) lang = "es";
  return dictionary[lang][key] || key;
}

const TRANSLATION_ENDPOINTS = [
  "https://translate.argosopentech.com/translate",
  "https://translate.terraprint.co/translate",
  "https://lt.vern.cc/translate"
];

export async function translateDynamic(text, targetLang = getLanguage()) {
  if (!text) return text;
  
  if (dictionary[targetLang] && dictionary[targetLang][text]) {
    return dictionary[targetLang][text];
  }

  if (targetLang === "es") {
    return text;
  }

  // Loop through public LibreTranslate instances with failover
  for (const endpoint of TRANSLATION_ENDPOINTS) {
    try {
      const res = await fetch(endpoint, {
        method: "POST",
        body: JSON.stringify({
          q: text,
          source: "es",
          target: "en",
          format: "text"
        }),
        headers: {
          "Content-Type": "application/json"
        }
      });
      
      if (res.ok) {
        const data = await res.json();
        if (data && data.translatedText) {
          const translated = data.translatedText;
          if (!dictionary[targetLang]) dictionary[targetLang] = {};
          dictionary[targetLang][text] = translated;
          return translated;
        }
      }
    } catch (error) {
      console.warn(`Translation failed for endpoint ${endpoint}:`, error);
    }
  }

  return text;
}
